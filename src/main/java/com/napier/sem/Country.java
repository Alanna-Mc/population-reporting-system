package com.napier.sem;

/**
 * Represents a Country
 */
public class Country
{
    /**
     * Country code
     */
    public String code;

    /**
     * Country name
     */
    public String name;

    /**
     * Country continent
     */
    public String continent;

    /**
     * Country region
     */
    public String region;

    /**
     * Country population
     */
    public int population;

    /**
     * Country capital
     */
    public Integer capital;
}
