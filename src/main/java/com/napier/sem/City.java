package com.napier.sem;

/**
 * Represents a City
 */
public class City
{
    /**
     * City Name
     */
    public String name;

    /**
     * City Country
     */
    public String country;

    /**
     * City District
     */
    public String district;

    /**
     * City population
     */
    public int population;

    /**
     * City continent
     */
    public String continent;

    /**
     * City region
     */
    public String region;
}
